    private void display() {
        viewNUID.setText(Integer.toString(this.person.getNUID()));
        viewName.setText(this.person.getFirstName() + " " + this.person.getLastName());
        viewCollegeName.setText(this.person.getCollegeName());
        
        viewOfficePhone.setText(String.valueOf(this.person.getOfficeContactInfo().getPhoneNumber()));
        viewOfficeEmail.setText(this.person.getOfficeContactInfo().getEmailID());
        
        viewPersonalPhone.setText(String.valueOf(this.person.getPersonalContactInfo().getPhoneNumber()));
        viewPersonalEmail.setText(this.person.getPersonalContactInfo().getEmailID());
        
        viewPermanentStreet.setText(this.person.getPermAddress().getStreetName());
        viewPermanentAptNum.setText(this.person.getPermAddress().getAptNumber());
        viewPermanentCityState.setText(this.person.getPermAddress().getCityState());
        viewPermanentZipCode.setText(Integer.toString(this.person.getPermAddress().getZipCode()));
        
        viewCurrentStreet.setText(this.person.getCurrentAddress().getStreetName());
        viewCurrentAptNum.setText(this.person.getCurrentAddress().getAptNumber());
        viewCurrentCityState.setText(this.person.getCurrentAddress().getCityState());
        viewCurrentZipCode.setText(Integer.toString(this.person.getCurrentAddress().getZipCode()));
    }
