    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // TODO add your handling code here:
        int NUID = Integer.valueOf(fieldNUID.getText());
        String firstName = fieldFirstName.getText();
        String lastName = fieldLastName.getText();
        String collegeName = fieldCollegeName.getText();
        
        long officePhone = Long.valueOf(fieldOfficePhone.getText());
        String officeEmail = fieldOfficeEmail.getText();
        
        long personalPhone = Long.valueOf(fieldPersonalPhone.getText());
        String personalEmail = fieldPersonalEmail.getText();
        
        String permStreet = fieldPermanentStreet.getText();
        String permAptNumber = fieldPermanentAptNum.getText();
        String permCityState = fieldPermanentCityState.getText();
        int permZipCode = Integer.valueOf(fieldPermanentZipCode.getText());
        
        String currentStreet = fieldCurrentStreet.getText();
        String currentAptNumber = fieldCurrentAptNum.getText();
        String currentCityState = fieldCurrentCityState.getText();
        int currentZipCode = Integer.valueOf(fieldCurrentZipCode.getText());
        
        this.person.setNUID(NUID);
        this.person.setFirstName(firstName);
        this.person.setLastName(lastName);
        this.person.setCollegeName(collegeName);
        
        ContactInformation officeContact = this.person.getOfficeContactInfo();
        officeContact.setPhoneNumber(officePhone);
        officeContact.setEmailID(officeEmail);
        
        ContactInformation personalContact = this.person.getPersonalContactInfo();
        personalContact.setPhoneNumber(personalPhone);
        personalContact.setEmailID(personalEmail);
        
        Address permAddress = this.person.getPermAddress();
        permAddress.setStreetName(permStreet);
        permAddress.setAptNumber(permAptNumber);
        permAddress.setCityState(permCityState);
        permAddress.setZipCode(permZipCode);
        
        Address currentAddress = this.person.getCurrentAddress();
        currentAddress.setStreetName(currentStreet);
        currentAddress.setAptNumber(currentAptNumber);
        currentAddress.setCityState(currentCityState);
        currentAddress.setZipCode(currentZipCode);
        
        
        JOptionPane.showMessageDialog(null, "SAVED SUCCESSFULLY!");
        
        display();
        
    }//GEN-LAST:event_SaveBtnActionPerformed

