    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        UserAccountDirectory uad = this.business.getUad();

        if(uad.accountExists(fieldUsername1.getText(), fieldPassword1.getText(), "agent")){
            JOptionPane.showMessageDialog(null, "sorry credential are taken");
        }else{
            UserAccount user = this.business.getUad().createUserAccount(fieldUsername1.getText(), fieldPassword1.getText(), "agent");
            this.business.getDad().createDeliveryAgent(user.getAccountId(), fieldName1.getText(), fieldAge1.getText());
            populate();
        }
    }//GEN-LAST:event_btnAddActionPerformed
    public void populate(){
        tableModel.setRowCount(0);
        for(DeliveryAgent da: this.business.getDad().getAgentList()){
            UserAccount u = this.business.getUad().findById(da.getPersonID());
            
            Object[] row = new Object[4];
            row[0] = da.getPersonID();
            row[1] = da.getName();
            row[2] = u.getUsername();
            row[3] = u.getPassword();
            
            tableModel.addRow(row);
        }
    }
